package com.diana.penjualanperhiasan.model;

public class ModelPerhiasan {

    String _id, kodePerhiasan, jenisPerhiasan, kadarPerhiasan, beratPerhiasan, hargaPerhiasan, gambar;

    public String get_id() {
        return _id;
    }

    public void set_id(String _id) {
        this._id = _id;
    }

    public String getKodePerhiasan() {
        return kodePerhiasan;
    }

    public void setKodePerhiasan(String kodePerhiasan) {
        this.kodePerhiasan = kodePerhiasan;
    }

    public String getJenisPerhiasan() {
        return jenisPerhiasan;
    }

    public void setJenisPerhiasan(String jenisPerhiasan) {
        this.jenisPerhiasan = jenisPerhiasan;
    }

    public String getKadarPerhiasan() {
        return kadarPerhiasan;
    }

    public void setKadarPerhiasan(String kadarPerhiasan) {
        this.kadarPerhiasan = kadarPerhiasan;
    }

    public String getBeratPerhiasan() {
        return beratPerhiasan;
    }

    public void setBeratPerhiasan(String beratPerhiasan) {
        this.beratPerhiasan = beratPerhiasan;
    }

    public String getHargaPerhiasan() {
        return hargaPerhiasan;
    }

    public void setHargaPerhiasan(String hargaPerhiasan) {
        this.hargaPerhiasan = hargaPerhiasan;
    }

    public String getGambar() {
        return gambar;
    }

    public void setGambar(String gambar) {
        this.gambar = gambar;
    }
}
